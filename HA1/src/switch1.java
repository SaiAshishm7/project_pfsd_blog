public class switch1 {
    public static void main(String[] args) {
        for (int i = 1; i <= 6; i++) {
            switch (i) {
                case 1:
                    System.out.println(" i is zero");
                    break;
                case 2:
                    System.out.println(" i is one");
                    break;
                case 3:
                    System.out.println(" i is two");
                    break;
                default:
                    System.out.println("i is greater than 3");

            }
        }
    }
}
